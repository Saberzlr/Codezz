# \t 制表符
print("jack\t20")

# \n 换行
print("Hello,jack\nHello,tom")

# \\ 输出\
print("D:python\\chapter\\02")
print("D:python\\\\chapter\\\\02")

# \" 输出"
print("People say:\"Hello World!\"")

# \' 输出' 单引号也可以不使用转义
print("People say:'ni hao'")

# \r 回车  使输入光标回到当前行的行首，相当于重新输入
print("我的灵儿是人间仙女，\r自然与其他人不一样")