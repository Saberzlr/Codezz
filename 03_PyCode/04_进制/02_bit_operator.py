print(~-2)      # 1
print(~2)       # -3
print(2 & 3)    # 2
print(2 ^ -3)   # -1
print(2 | 3)    # 3
print(5 << 1)   # 10
print(-5 << 1)  # -10
print(5 >> 1)   # 2
print(-5 >> 1)  # -3
print(8 >> 2)   # 2
print(-13 >> 2) # -4    -13 // 2 = min{-6, -7} = -7 // 2 = min{-3, -4} = -4


# 思考题
a = 1 >> 2  # 1 // 2 = min{0, 1} = 0
'0000 0001 => 0000 0001 => 0000 0000 => 0000 0000 => 0'
b = -1 >> 2 # -1 // 2 = min{-1, 0} = -1
'1000 0001 => 1111 1111 => 1111 1111 => 1000 0001 => -1'
c = 1 << 2  # 4
'0000 0001 => 0000 0001 => 0000 0100 => 0000 0100 => 4'
d = -1 << 2 # -4
'1000 0001 => 1111 1111 => 1111 1100 => 1000 0100 => -4'

# 输出a, b, c, d的结果
print("a =", a)
print("b =", b)
print("c =", c)
print("d =", d)

