# 打印1-100之间所有能被3整除的数
'''
    思路分析:
    1. 定义变量i + while循环能输出1 - 100之间的所有的数
    2. 循环终止条件: while (i < 100)
    3. 输出条件: i % 3 == 0
    4. 进行输出
'''
i = 1
while (i < 100):
    if i % 3 == 0:
        print(i)
    i += 1